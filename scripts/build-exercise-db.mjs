// Regenerates supabase/functions/_shared/exerciseDb.ts from the
// exercises-dataset JSON (https://github.com/hasaneyldrm/exercises-dataset,
// MIT). Only name + mapped muscle group are kept - no instructions, no media
// (the media there is not MIT). Usage:
//   curl -L https://raw.githubusercontent.com/hasaneyldrm/exercises-dataset/main/data/exercises.json -o /tmp/exercises.json
//   node scripts/build-exercise-db.mjs /tmp/exercises.json

import { readFileSync, writeFileSync } from 'node:fs'

// dataset `target` (the reliable field; its `muscle_group` is noisy) ->
// this app's muscle vocabulary (PlanEditor MUSCLE_GROUPS)
const TARGET_TO_MUSCLE = {
  pectorals: 'Chest',
  'serratus anterior': 'Chest',
  delts: 'Shoulders',
  traps: 'Shoulders',
  'levator scapulae': 'Shoulders',
  triceps: 'Triceps',
  'upper back': 'Back',
  lats: 'Back',
  spine: 'Back',
  biceps: 'Biceps',
  quads: 'Quads',
  adductors: 'Quads',
  hamstrings: 'Hamstrings',
  glutes: 'Glutes',
  abductors: 'Glutes',
  calves: 'Calves',
  abs: 'Other',
  forearms: 'Other',
  'cardiovascular system': null, // cardio entries don't belong in a strength picker
}

// The dataset targets the squat/press/lunge family at glutes (anatomically
// arguable, but it fights both gym convention and this app's own seeded plan,
// where Leg Press and Hack Squat count as Quads). Name-based overrides win
// over the target mapping; first match applies.
const NAME_OVERRIDES = [
  [/romanian|stiff.?leg|good morning/i, 'Hamstrings'],
  [/squat|leg press|lunge|step.?up|split.?squat/i, 'Quads'],
  [/calf|heel raise/i, 'Calves'],
]

const src = process.argv[2]
if (!src) {
  console.error('usage: node scripts/build-exercise-db.mjs <exercises.json>')
  process.exit(1)
}

const raw = JSON.parse(readFileSync(src, 'utf-8'))
const list = Array.isArray(raw) ? raw : raw.exercises

const seen = new Set()
const rows = []
const unmapped = new Set()
for (const e of list) {
  const muscle = TARGET_TO_MUSCLE[e.target]
  if (muscle === null) continue
  if (muscle === undefined) {
    unmapped.add(e.target)
    continue
  }
  const key = e.name.toLowerCase()
  if (seen.has(key)) continue
  seen.add(key)
  const override = NAME_OVERRIDES.find(([re]) => re.test(e.name))
  rows.push([e.name, override ? override[1] : muscle])
}
if (unmapped.size > 0) {
  console.error('unmapped targets (add them to TARGET_TO_MUSCLE):', [...unmapped])
  process.exit(1)
}
rows.sort((a, b) => a[0].localeCompare(b[0]))

const body = rows.map(([n, m]) => JSON.stringify([n, m])).join(',\n  ')
const out = `// GENERATED by scripts/build-exercise-db.mjs - do not edit by hand.
// Source: https://github.com/hasaneyldrm/exercises-dataset (MIT license).
// Only exercise names + muscle groups (mapped to this app's vocabulary) are
// kept; the dataset's media and instructions are deliberately excluded.
// Imported by the browser (lazy chunk, excluded from the PWA precache) AND
// by the interpret edge function - keep it dependency-free.

export type ExerciseEntry = [name: string, muscle: string]

export const EXERCISE_DB: ExerciseEntry[] = [
  ${body},
]

const norm = (s: string) => s.toLowerCase().replace(/[^a-z0-9]/g, '')

// gym shorthand people actually type/say -> the dataset's vocabulary
const ALIASES: Record<string, string> = { db: 'dumbbell', bb: 'barbell', ohp: 'overhead press', rdl: 'romanian deadlift' }

const tokenize = (s: string) =>
  s
    .toLowerCase()
    .split(/[^a-z0-9]+/)
    .filter(Boolean)
    .map((w) => ALIASES[w] ?? w)

let normIndex: [string, string][] | null = null

/** Muscle group for a (freeform, user-typed) exercise name. Exact normalized
 *  match first; then the entry the query extends ("incline barbell bench
 *  press" -> "barbell bench press", longest wins = most specific); then the
 *  shortest entry that extends the query ("leg press" -> the plain "sled 45°
 *  leg press", not "...calf press on leg press"). Null when nothing plausibly
 *  matches - never guess. */
export function muscleForExercise(name: string): string | null {
  if (!normIndex) normIndex = EXERCISE_DB.map(([n, m]) => [norm(n), m])
  const q = norm(name)
  if (q.length < 4) return null
  const exact = normIndex.find(([n]) => n === q)
  if (exact) return exact[1]
  let inside: [string, string] | null = null // entry contained in the query
  let around: [string, string] | null = null // entry containing the query
  for (const entry of normIndex) {
    if (q.includes(entry[0])) {
      if (!inside || entry[0].length > inside[0].length) inside = entry
    } else if (entry[0].includes(q)) {
      if (!around || entry[0].length < around[0].length) around = entry
    }
  }
  if (inside || around) return (inside ?? around)![1]
  // word order varies ("incline dumbbell curl" vs "dumbbell incline curl") and
  // people abbreviate ("incline DB curl") - all query words in any order, the
  // shortest entry that holds them all wins
  const tokens = tokenize(name).map(norm).filter((w) => w.length >= 3)
  if (tokens.length < 2) return null
  let byTokens: [string, string] | null = null
  for (const entry of normIndex) {
    if (tokens.every((w) => entry[0].includes(w))) {
      if (!byTokens || entry[0].length < byTokens[0].length) byTokens = entry
    }
  }
  return byTokens ? byTokens[1] : null
}
`
writeFileSync('supabase/functions/_shared/exerciseDb.ts', out)
console.log(`wrote ${rows.length} exercises`)
